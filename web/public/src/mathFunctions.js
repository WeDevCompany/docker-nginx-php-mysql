/**
 * @param num1 {number}
 * @param num2 {number}
 * @returns {number}
 */
const add = ( num1, num2 ) => num1 + num2


/**
 * @param num1 {number}
 * @param num2 {number}
 * @returns {number}
 */
const subtract = ( num1, num2 ) => num1 - num2


module.exports = { add, subtract }
